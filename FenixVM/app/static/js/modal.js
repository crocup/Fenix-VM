$("#showModal").click(function() {
  $(".modal").addClass("is-active");
});

$(".modal-close").click(function() {
   $(".modal").removeClass("is-active");
});

$("#closebtn").click(function() {
   $(".modal").removeClass("is-active");
});

$("#closetop").click(function() {
   $(".modal").removeClass("is-active");
});